    private void jTree1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTree1MouseClicked
        TreePath tree = this.jTree1.getAnchorSelectionPath();
        if (tree == null) {
            return;
        }
        DefaultMutableTreeNode selNode = (DefaultMutableTreeNode) this.jTree1.getSelectionPath().getLastPathComponent();
        Object selecBook = selNode.getUserObject();
        if (selecBook instanceof Book) {
            Book book = (Book) selecBook;
            jBookID.setText(book.getBookID());
            jTitle.setText(book.getTitle());
            jAuthor.setText(book.getAuthor());
            jCategory.setText(book.getCategory());
            jKeyword.setText(book.getKeyword());
        }
    }//GEN-LAST:event_jTree1MouseClicked

