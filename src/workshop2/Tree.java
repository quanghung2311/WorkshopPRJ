    public void createTree() {
        DefaultMutableTreeNode top = new DefaultMutableTreeNode("Fulib");
        ArrayList<Model.Book> list;
        ArrayList<String> lcate = new ArrayList<>();
        list = BookDB.display();
        for (Book bk : list) {
            String subNode = bk.getCategory();
            if (!lcate.contains(subNode)) {
                lcate.add(subNode);
                DefaultMutableTreeNode treeNode = new DefaultMutableTreeNode(subNode);
                top.add(treeNode);
                for (Book book : list) {
                    if (book.getCategory().equals(bk.getCategory())) {
                        treeNode.add(new DefaultMutableTreeNode(book));
                    }
                }
            }
        }
        tree = new DefaultTreeModel(top);
        jTree1.setModel(tree);

    }

